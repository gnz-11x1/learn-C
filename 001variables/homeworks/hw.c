#include <stdio.h>
#include <stdbool.h>

int main() {
    int noOfLoves = 100; // int data type
    printf("I love you %d times\n", noOfLoves);
    float noOfParallelUniverse = 3.69; // float data type
    printf("in %f parallel universes\n", noOfParallelUniverse);
    double withHeartBeat = 3.6969696969696969; // double data type
    printf("with a heart beat of %lf cause I'm a alien\n", withHeartBeat);
    char language = 'C'; // char data type
    printf("Today is my first day of learning programming, also in %c\n", language);
    char yesBro[] = "btw, You're doing a great job, man, I completed my hw, I don't think you'll call police anymore"; // char[] 
    printf("%s\n", yesBro);
    bool doWeLoveYou = true; // bool data type
    printf("btw, yes we love you %d\n", doWeLoveYou);

    return 0;
}